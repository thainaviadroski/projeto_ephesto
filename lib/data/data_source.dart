const String DATABASE_NAME = 'mycar.db';

const String USER_TABLE_NAME = 'user';
const String USER_COLUMN_ID = 'id';
const String USER_COLUMN_NOME_COMPLETO = 'nomeCompleto';
const String USER_COLUMN_EMAIL = 'email';
const String USER_COLUMN_SENHA = 'senha';

const String CAR_TABLE_NAME = 'car';
const String CAR_COLUMN_ID = 'id';
const String CAR_COLUMN_ID_USER = 'idUser';
const String CAR_COLUMN_MARCA = 'marca';
const String CAR_COLUMN_MODELO = 'modelo';
const String CAR_COLUMN_ANO = 'ano';
const String CAR_COLUMN_PLACA = 'placa';

const String FUEL_TABLE_NAME = 'fuel';
const String FUEL_COLUMN_ID = 'id';
const String FUEL_COLUMN_VALOR_LITRO = 'valorLitro';
const String FUEL_COLUMN_VALOR_TOTAL_GASTO = 'valorTotalGasto';
const String FUEL_COLUMN_PLACA = 'placa';

const String CREATE_USER_TABLE_SCRIPT = '''
CREATE TABLE IF NOT EXISTS  $USER_TABLE_NAME(
  $USER_COLUMN_ID INTEGER PRIMARY KEY AUTOINCREMENT,
  $USER_COLUMN_NOME_COMPLETO TEXT,
  $USER_COLUMN_EMAIL TEXT,
  $USER_COLUMN_SENHA TEXT
)
''';

const String CREATE_CAR_TABLE_SCRIPT = '''
CREATE TABLE IF NOT EXISTS  $CAR_TABLE_NAME(
  $CAR_COLUMN_ID INTEGER PRIMARY KEY AUTOINCREMENT,
  $CAR_COLUMN_ID_USER INTEGER,
  $CAR_COLUMN_MARCA TEXT,
  $CAR_COLUMN_MODELO TEXT,
  $CAR_COLUMN_ANO INTEGER,
  $CAR_COLUMN_PLACA TEXT,
  FOREIGN KEY ($CAR_COLUMN_ID_USER) REFERENCES $USER_TABLE_NAME ($USER_COLUMN_ID)
)
''';

const String CREATE_FUEL_TABLE_SCRIPT = '''
CREATE TABLE IF NOT EXISTS  $FUEL_TABLE_NAME IF NOT EXISTS (
  $FUEL_COLUMN_ID INTEGER PRIMARY KEY AUTOINCREMENT,
  $FUEL_COLUMN_VALOR_LITRO REAL,
  $FUEL_COLUMN_VALOR_TOTAL_GASTO REAL,
  $FUEL_COLUMN_PLACA TEXT,
  FOREIGN KEY ($FUEL_COLUMN_PLACA) REFERENCES $CAR_TABLE_NAME ($CAR_COLUMN_PLACA)
)
''';